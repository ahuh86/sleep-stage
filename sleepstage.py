# ============================================================
# sleepstage.py — 睡眠阶段标签定义
# 
# 【这个文件是干什么的？】
# 在睡眠分期任务中，我们需要把每30秒的脑电信号（称为一个"epoch"）
# 归类到不同的睡眠阶段。这个文件就定义了这些"类别"是什么。
#
# 【AASM标准】
# 采用美国睡眠医学会（AASM）制定的R&K标准，
# 这是全球睡眠医学领域最通用的分期体系。
# 人类专家通过观察脑电波（EEG）、眼电（EOG）、肌电（EMG）的特征波形
# 来做出判断——而我们训练深度学习模型来"学习"这种判断能力。
#
# 【睡眠阶段的正常循环】
# 一个完整的睡眠周期约90分钟，顺序通常是：
#   Wake → N1 → N2 → N3 → N2 → REM → N2 → N3 → ... （循环4-6次）
# 深度学习模型正是利用这种"顺序规律"来提高预测准确率。
# ============================================================

# ==================== 标签数值定义 ====================

W = 0       # 清醒期（Wake）
# 特征：大脑活跃，脑电波以高频低幅的beta波为主
# 此时人处于清醒状态，眼球可能快速运动，肌肉有张力

N1 = 1      # N1期（浅睡眠第1阶段）
# 特征：从清醒到睡眠的过渡阶段，theta波（4-7Hz）逐渐增多
# 人容易惊醒，感觉自己在"漂浮"或"做梦般的画面闪现"
# 通常只占整夜睡眠的5%左右，是最难分类的阶段（样本少+特征模糊）

N2 = 2      # N2期（浅睡眠第2阶段）
# 特征：出现"睡眠纺锤波"（sleep spindle，12-14Hz短暂爆发）
# 和"K复合波"（K-complex，一个大的慢波+快波组合）
# 这是占比最大的睡眠阶段，约占整夜的45-55%

N3 = 3      # N3期（深睡眠 / 慢波睡眠）
# 特征：脑电波以高幅低频的delta波（0.5-4Hz）为主
# 又称"慢波睡眠"，人最难被叫醒
# 对恢复体力、巩固记忆至关重要
# 随着年龄增长，N3的比例会显著下降

REM = 4     # 快速眼动期
# 特征：眼球快速左右运动，脑电波看起来像清醒时（" paradoxical sleep"）
# 这是做梦的主要阶段，大脑非常活跃但身体肌肉处于瘫痪状态
# 约占整夜睡眠的20-25%

MOVE = 5    # 体动（Movement）
# 身体移动造成的信号干扰，无法判断睡眠阶段
# 在实际训练中通常被忽略或归为Wake

UNK = 6     # Unknown（未知）
# 信号质量差或其他原因导致无法分期的片段

# ==================== 名称到数值的映射 ====================
# 用于从数据文件中的字符串标签（如"W"）转换为整数
stage_dict = {
    "W": W,
    "N1": N1,
    "N2": N2,
    "N3": N3,
    "REM": REM,
    "MOVE": MOVE,
    "UNK": UNK,
}

# ==================== 数值到名称的映射 ====================
# 用于将模型的预测结果（整数）转换为人类可读的字符串
class_dict = {
    W: "W",
    N1: "N1",
    N2: "N2",
    N3: "N3",
    REM: "REM",
    MOVE: "MOVE",
    UNK: "UNK",
}
